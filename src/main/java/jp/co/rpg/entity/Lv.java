package jp.co.rpg.entity;

public class Lv {

	private Integer id;
	private Integer lv;
	private Integer needXp;
	private Integer adjustPoint;

	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getLv() {
		return lv;
	}
	public void setLv(Integer lv) {
		this.lv = lv;
	}
	public Integer getNeedXp() {
		return needXp;
	}
	public void setNeedXp(Integer needXp) {
		this.needXp = needXp;
	}
	public Integer getAdjustPoint() {
		return adjustPoint;
	}
	public void setAdjustPoint(Integer adjustPoint) {
		this.adjustPoint = adjustPoint;
	}

}
